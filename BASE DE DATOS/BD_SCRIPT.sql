  CREATE FUNCTION [dbo].[CalcularDias] (
    @INICIO DATE,
    @FIN DATE
)
RETURNS INT
AS
BEGIN
    DECLARE @BusinessDays INT;

    WITH FECHASRANGO AS (
        SELECT @INICIO AS DateValue
        UNION ALL
        SELECT DATEADD(DAY, 1, DateValue)
        FROM FECHASRANGO 
        WHERE DateValue < @FIN
    )
    SELECT @BusinessDays = COUNT(*)
    FROM FECHASRANGO 
    WHERE DateValue >= @INICIO AND DateValue <= @FIN -- Dentro del rango de fechas
        AND DATEPART(WEEKDAY, DateValue) NOT IN (1, 7); -- Excluye domingos (1) y sÃ¡bados (7)

    RETURN @BusinessDays;
END;
go

